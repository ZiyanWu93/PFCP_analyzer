size_t ReadHexFile(FILE *inf, unsigned char *dest) {
    size_t count = 0;
    int n;
    if (dest == NULL) {
        unsigned char OneByte;
        while ((n = fscanf(inf, "%hhx", &OneByte)) == 1) {
            count++;
        }
    } else {
        while ((n = fscanf(inf, "%hhx", dest)) == 1) {
            dest++;
        }
    }
    if (n != EOF) { ;  // handle syntax error
    }
    return count;
}


